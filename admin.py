# ── Password hashing (PBKDF2-HMAC-SHA256) ────────────────────────────────────
# Format:  pbkdf2_sha256$<iterations>$<salt_hex>$<hash_hex>
# Generated by scripts/hash_password.py and the admin Settings tab. Verified
# in constant time via hmac.compare_digest.
_PBKDF2_ITERATIONS = 600_000
_PBKDF2_DKLEN = 32
_PBKDF2_SALT_BYTES = 16


def _hash_password(plain: str) -> str:
    salt = secrets.token_bytes(_PBKDF2_SALT_BYTES)
    dk = hashlib.pbkdf2_hmac(
        "sha256", plain.encode("utf-8"), salt, _PBKDF2_ITERATIONS, dklen=_PBKDF2_DKLEN
    )
    return f"pbkdf2_sha256${_PBKDF2_ITERATIONS}${salt.hex()}${dk.hex()}"


def _verify_password_hash(plain: str, encoded: str) -> bool:
    try:
        scheme, iters_str, salt_hex, hash_hex = encoded.split("$", 3)
    except ValueError:
        return False
    if scheme != "pbkdf2_sha256":
        return False
    try:
        iters = int(iters_str)
        salt = bytes.fromhex(salt_hex)
        expected = bytes.fromhex(hash_hex)
    except ValueError:
        return False
    actual = hashlib.pbkdf2_hmac("sha256", plain.encode("utf-8"), salt, iters, dklen=len(expected))
    return hmac.compare_digest(actual, expected)


def _current_admin_hash() -> str:
    """Resolve the active admin hash: override file > env var > empty."""
    try:
        if ADMIN_PASSWORD_FILE.is_file():
            return ADMIN_PASSWORD_FILE.read_text(encoding="utf-8").strip()
    except OSError:
        pass
    return ADMIN_HASH


def _check_password(plain: str) -> bool:
    active_hash = _current_admin_hash()
    if active_hash:
        return _verify_password_hash(plain, active_hash)
    # Plaintext fallback for first-boot only. constant-time compare.
    return bool(ADMIN_PASS) and hmac.compare_digest(plain, ADMIN_PASS)

