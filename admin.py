@router.get("/api/cert")
async def api_cert(ag_session: str | None = Cookie(None)):
    if not _valid(ag_session):
        return JSONResponse({"error": "unauthorized"}, status_code=401)

    cert_path = CERT_DIR / "fullchain.pem"
    if not cert_path.is_file():
        return JSONResponse({"error": f"Certificate not found at {cert_path}. "
                                       "Run scripts/bootstrap.sh on the host or place "
                                       "your own fullchain.pem there."}, status_code=200)
    try:
        from cryptography import x509
        from cryptography.x509.oid import ExtensionOID, NameOID
        from datetime import datetime, timezone

        data = cert_path.read_bytes()
        cert = x509.load_pem_x509_certificate(data)

        def _name_str(name) -> str:
            return ", ".join(f"{a.oid._name}={a.value}" for a in name)

        try:
            san_ext = cert.extensions.get_extension_for_oid(ExtensionOID.SUBJECT_ALTERNATIVE_NAME)
            san_parts = []
            for entry in san_ext.value:
                cls = type(entry).__name__
                prefix = "DNS" if cls == "DNSName" else "IP" if cls == "IPAddress" else cls
                san_parts.append(f"{prefix}:{entry.value}")
            san = ", ".join(san_parts) if san_parts else "(none)"
        except x509.ExtensionNotFound:
            san = "(none)"

        not_before = cert.not_valid_before_utc
        not_after  = cert.not_valid_after_utc
        days_remaining = int((not_after - datetime.now(timezone.utc)).total_seconds() // 86400)

        subject = _name_str(cert.subject)
        issuer  = _name_str(cert.issuer)
        self_signed = subject == issuer

        return JSONResponse({
            "subject": subject,
            "issuer":  issuer,
            "serial":  format(cert.serial_number, "x"),
            "san":     san,
            "not_before": not_before.strftime("%Y-%m-%d %H:%M UTC"),
            "not_after":  not_after.strftime("%Y-%m-%d %H:%M UTC"),
            "days_remaining": days_remaining,
            "self_signed": self_signed,
        })
    except Exception as exc:
        return JSONResponse({"error": f"Failed to parse {cert_path}: {exc}"}, status_code=200)


@router.get("/api/cert/renew-command")
async def api_cert_renew_command(ag_session: str | None = Cookie(None)):
    """Return the shell command the operator should run on the host to renew.

    We deliberately don't auto-execute: the certbot run needs HTTP-01 reachable
    on port 80 and produces output the operator should see directly. Auto-exec
    inside this container would also need bind-mounted host paths that we can't
    portably resolve from inside the container.
    """
    if not _valid(ag_session):
        return JSONResponse({"error": "unauthorized"}, status_code=401)

    if TLS_MODE == "letsencrypt":
        cmd = ("docker compose --profile letsencrypt run --rm certbot && \\\n"
               "  docker compose restart nginx kafka-cert-init kafka")
        note = ("Run this from the repository root on the Docker host. "
                "Requires DNS for the domain to point at this host and port 80 reachable.")
    elif TLS_MODE == "self-signed":
        cmd = (f"./scripts/gen-self-signed.sh {DOMAIN} && \\\n"
               "  docker compose restart nginx kafka-cert-init kafka")
        note = ("Self-signed cert mode. Re-running the generator produces a fresh "
                "825-day cert; restart nginx + kafka to pick it up.")
    else:
        cmd = ("# TLS_MODE=provided — replace these files manually:\n"
               f"#   {CERT_DIR}/fullchain.pem\n"
               f"#   {CERT_DIR}/privkey.pem\n"
               "docker compose restart nginx kafka-cert-init kafka")
        note = "TLS mode is 'provided'. Replace the files yourself, then restart."

    return JSONResponse({"command": cmd, "note": note, "tls_mode": TLS_MODE})


@router.post("/api/change-password")
async def api_change_password(
    current: str = Form(...),
    new:     str = Form(...),
    ag_session: str | None = Cookie(None),
):
    if not _valid(ag_session):
        return JSONResponse({"error": "unauthorized"}, status_code=401)
    if not _check_password(current):
        return JSONResponse({"error": "Current password is incorrect."}, status_code=400)
    if len(new) < 8:
        return JSONResponse({"error": "New password must be at least 8 characters."}, status_code=400)

    new_hash = _hash_password(new)
    try:
        ADMIN_PASSWORD_FILE.parent.mkdir(parents=True, exist_ok=True)
        # Atomic write: rename over the target so a crash mid-write can't leave
        # a half-written file that locks out logins.
        tmp = ADMIN_PASSWORD_FILE.with_suffix(".tmp")
        tmp.write_text(new_hash + "\n", encoding="utf-8")
        tmp.chmod(0o600)
        tmp.replace(ADMIN_PASSWORD_FILE)
    except OSError as exc:
        return JSONResponse({"error": f"Could not persist new hash: {exc}"}, status_code=500)

    return JSONResponse({"ok": True})
